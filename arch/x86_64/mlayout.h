#ifndef _ARCH_X86_64_MLAYOUT_H_
#define _ARCH_X86_64_MLAYOUT_H_

#define PADDR_TOP(prefix) ((prefix##_PBASE) + ((prefix##_SIZE) - 1))
#define VADDR_TOP(prefix) ((prefix##_VBASE) + ((prefix##_SIZE) - 1))

enum {
	USER_VBASE = 0x0000000000001000ULL,
	USER_SIZE = (0x0000800000000000ULL - USER_VBASE),
	USER_VTOP = VADDR_TOP(USER),

	//
	// User Image Area
	//
	UIMAGE_VBASE = 0x0000000000400000ULL,
	UIMAGE_SIZE = (0x0000000030000000ULL - UIMAGE_VBASE),
	UIMAGE_VTOP = VADDR_TOP(UIMAGE),

	KSPACE_VBASE = 0xffff800000000000ULL,
	KSPACE_SIZE = 0x0000800000000000ULL,
	KSPACE_VTOP = (KSPACE_VBASE + (KSPACE_SIZE - 1)),

	//
	// Direct Physical Memory Mapping
	//
	DIRECTPHYMEM_VBASE = 0xffff800000000000ULL,
	DIRECTPHYMEM_SIZE = 0x0000400000000000ULL,
	DIRECTPHYMEM_VTOP = (DIRECTPHYMEM_VBASE + DIRECTPHYMEM_SIZE - 1),

	//
	// Kernel Image
	//
	KERNEL_VBASE = 0xffffffff80000000ULL,
	KERNEL_SIZE = 0x0000000010000000ULL,
	KERNEL_VTOP = (KERNEL_VBASE + KERNEL_SIZE - 1),

	KASAN_SHADOW_SIZE = 0x100000000000,
	KASAN_SHADOW_VTOP = (KERNEL_VBASE - 1),
	KASAN_SHADOW_VBASE = (KERNEL_VBASE - KASAN_SHADOW_SIZE),

	//
	// Kernel Top Page Tables
	//
	KINITPTT_SIZE = 0x00800000,	 // Maps the top 2GB area

	//
	// Kernel Initial Page Directory Table
	//
	KINITPDT_SIZE = 0x00008000,	 // Maps the bottom 4GB area

	//
	// Kernel Initial PDP Table
	//
	KINITPDPT_SIZE = 0x00004000,  // Maps the bottom 4GB area

	//
	// Kernel PML4 Table
	//
	KINITPML4T_SIZE = 0x00001000,  // Maps the bottom 4GB area

	//
	// Initializable Part of Critical Area
	//
	INIT_CRITICAL_VBASE = KERNEL_VBASE,
	INIT_CRITICAL_SIZE =
		(KERNEL_SIZE + KINITPML4T_SIZE + KINITPDPT_SIZE + KINITPDT_SIZE + KINITPTT_SIZE),
	INIT_CRITICAL_VTOP =
		(INIT_CRITICAL_VBASE + INIT_CRITICAL_SIZE - 1),

	//
	// Kernel Temporary Mapping Area
	//
	KINITTMPMAP_VBASE = (INIT_CRITICAL_VTOP + 1),
	KINITTMPMAP_SIZE = 0x00100000,
	KINITTMPMAP_VTOP = (KINITTMPMAP_VBASE + KINITTMPMAP_SIZE - 1)
};

#endif
